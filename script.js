let currentQuiz = [];
let currentIndex = 0;
let score = 0;
let locked = false;

const categoryButtons = document.getElementById('categoryButtons');
const quizArea = document.getElementById('quizArea');
const resultArea = document.getElementById('resultArea');
const progress = document.getElementById('progress');
const scoreEl = document.getElementById('score');
const examTitle = document.getElementById('examTitle');
const questionText = document.getElementById('questionText');
const choicesEl = document.getElementById('choices');
const explanation = document.getElementById('explanation');
const resultText = document.getElementById('resultText');
const restartBtn = document.getElementById('restartBtn');

function shuffle(array) {
  const a = [...array];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function createButtons() {
  const allBtn = document.createElement('button');
  allBtn.textContent = '全資格ミックス';
  allBtn.onclick = () => startQuiz('all');
  categoryButtons.appendChild(allBtn);
  Object.entries(exams).forEach(([key, name]) => {
    const btn = document.createElement('button');
    btn.textContent = name;
    btn.onclick = () => startQuiz(key);
    categoryButtons.appendChild(btn);
  });
}

function startQuiz(examKey) {
  const source = examKey === 'all' ? quizData : quizData.filter(q => q.exam === examKey);
  currentQuiz = shuffle(source).slice(0, Math.min(QUIZ_COUNT, source.length));
  currentIndex = 0;
  score = 0;
  locked = false;
  resultArea.classList.add('hidden');
  quizArea.classList.remove('hidden');
  showQuestion();
}

function showQuestion() {
  locked = false;
  const q = currentQuiz[currentIndex];
  progress.textContent = `${currentIndex + 1} / ${currentQuiz.length}`;
  scoreEl.textContent = `正解 ${score}`;
  examTitle.textContent = q.examName;
  questionText.textContent = q.question;
  choicesEl.innerHTML = '';
  explanation.classList.add('hidden');
  explanation.textContent = '';

  const mixed = shuffle(q.choices.map((text, index) => ({ text, originalIndex: index })));
  mixed.forEach(choice => {
    const btn = document.createElement('button');
    btn.textContent = choice.text;
    btn.onclick = () => answer(btn, choice.originalIndex, q.answer, q.explanation);
    choicesEl.appendChild(btn);
  });
}

function answer(button, selected, correct, exp) {
  if (locked) return;
  locked = true;
  const buttons = [...choicesEl.querySelectorAll('button')];
  buttons.forEach(btn => btn.disabled = true);

  if (selected === correct) {
    score++;
    button.classList.add('correct');
  } else {
    button.classList.add('wrong');
    buttons.forEach(btn => {
      const q = currentQuiz[currentIndex];
      if (btn.textContent === q.choices[correct]) btn.classList.add('correct');
    });
  }
  scoreEl.textContent = `正解 ${score}`;
  explanation.textContent = exp;
  explanation.classList.remove('hidden');

  setTimeout(() => {
    currentIndex++;
    if (currentIndex >= currentQuiz.length) showResult();
    else showQuestion();
  }, 1100);
}

function showResult() {
  quizArea.classList.add('hidden');
  resultArea.classList.remove('hidden');
  const rate = Math.round((score / currentQuiz.length) * 100);
  resultText.textContent = `${currentQuiz.length}問中 ${score}問正解（正答率${rate}%）です。`;
}

restartBtn.onclick = () => startQuiz('all');
createButtons();
